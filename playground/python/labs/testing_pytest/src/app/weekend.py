"""
weekend.py

このモジュールの目的は、「テストしにくいコード」と「テストしやすいコード」の違いを示すことです。
現在時刻のような外部依存を直接参照すると、テスト結果が実行タイミングに依存して不安定になります。
そのため、まず悪い例を示し、その後で改善した関数を示します。
"""

import datetime



def is_weekend_now() -> bool:
    """
    現在日時を直接参照して土日かどうかを判定します。

    この関数は実行日によって結果が変わるため、
    そのままでは再現性の高いテストを書きにくい例です。
    学習目的として「こういう形はテストしにくい」ということを理解するために残しています。
    """
    today = datetime.datetime.now().weekday()
    return today >= 5



def is_weekend(day: int) -> bool:
    """
    曜日番号を受け取り、土日かどうかを返します。

    weekday の一般的な表現に合わせ、
    0=月曜, 1=火曜, ..., 5=土曜, 6=日曜 としています。

    現在時刻を内部で取得せず、外から day を受け取ることで、
    テストが入力駆動になり、安定して実行できるようになります。
    これが「テストしやすい設計」の基本形です。
    """
    return day >= 5